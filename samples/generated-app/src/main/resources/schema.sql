-- =============================================================
-- Schema for Project Management System (プロジェクト管理システム)
-- =============================================================

-- ユーザ
CREATE TABLE IF NOT EXISTS USERS (
    USER_ID         SERIAL       PRIMARY KEY,
    KANJI_NAME      VARCHAR(128) NOT NULL,
    KANA_NAME       VARCHAR(128) NOT NULL,
    PM_FLAG         BOOLEAN      NOT NULL DEFAULT FALSE
);

-- システムアカウント
CREATE TABLE IF NOT EXISTS SYSTEM_ACCOUNT (
    USER_ID                   INTEGER      PRIMARY KEY REFERENCES USERS(USER_ID),
    LOGIN_ID                  VARCHAR(20)  NOT NULL,
    USER_PASSWORD             VARCHAR(256) NOT NULL,
    PASSWORD_EXPIRATION_DATE  DATE         NOT NULL,
    FAILED_COUNT              SMALLINT     NOT NULL DEFAULT 0,
    EFFECTIVE_DATE_FROM       DATE,
    EFFECTIVE_DATE_TO         DATE,
    LAST_LOGIN_DATE_TIME      TIMESTAMP
);
CREATE UNIQUE INDEX IF NOT EXISTS IDX_SYSTEM_ACCOUNT_LOGIN_ID ON SYSTEM_ACCOUNT(LOGIN_ID);

-- 組織
CREATE TABLE IF NOT EXISTS ORGANIZATION (
    ORGANIZATION_ID    SERIAL       PRIMARY KEY,
    ORGANIZATION_NAME  VARCHAR(128) NOT NULL,
    UPPER_ORGANIZATION INTEGER      REFERENCES ORGANIZATION(ORGANIZATION_ID)
);

-- Client (顧客)
CREATE TABLE IF NOT EXISTS CLIENT (
    CLIENT_ID     SERIAL       PRIMARY KEY,
    CLIENT_NAME   VARCHAR(128) NOT NULL,
    INDUSTRY_CODE VARCHAR(2)
);

-- プロジェクト
CREATE TABLE IF NOT EXISTS PROJECT (
    PROJECT_ID         SERIAL       PRIMARY KEY,
    PROJECT_NAME       VARCHAR(128) NOT NULL,
    PROJECT_TYPE       VARCHAR(2)   NOT NULL,
    PROJECT_CLASS      VARCHAR(2)   NOT NULL,
    PROJECT_START_DATE DATE         NOT NULL,
    PROJECT_END_DATE   DATE         NOT NULL,
    ORGANIZATION_ID    INTEGER      NOT NULL REFERENCES ORGANIZATION(ORGANIZATION_ID),
    CLIENT_ID          INTEGER      NOT NULL,
    PROJECT_MANAGER    VARCHAR(128) NOT NULL,
    PROJECT_LEADER     VARCHAR(128) NOT NULL,
    NOTE               VARCHAR(512),
    SALES              INTEGER,
    VERSION_NO         BIGINT       NOT NULL DEFAULT 1
);

-- プロジェクト担当者
CREATE TABLE IF NOT EXISTS PROJECT_USER (
    PROJECT_ID INTEGER NOT NULL REFERENCES PROJECT(PROJECT_ID),
    USER_ID    INTEGER NOT NULL REFERENCES USERS(USER_ID),
    PRIMARY KEY (PROJECT_ID, USER_ID)
);

-- 業務日付
CREATE TABLE IF NOT EXISTS BUSINESS_DATE (
    SEGMENT_ID VARCHAR(2)  PRIMARY KEY,
    BIZ_DATE   VARCHAR(8)  NOT NULL
);

-- コード名称
CREATE TABLE IF NOT EXISTS CODE_NAME (
    CODE_ID    VARCHAR(8)  NOT NULL,
    CODE_VALUE VARCHAR(2)  NOT NULL,
    LANG       VARCHAR(2)  NOT NULL,
    SORT_ORDER VARCHAR(1),
    CODE_NAME  VARCHAR(50),
    SHORT_NAME VARCHAR(50),
    OPTION01   VARCHAR(40),
    OPTION02   VARCHAR(40),
    OPTION03   VARCHAR(40),
    OPTION04   VARCHAR(40),
    OPTION05   VARCHAR(40),
    OPTION06   VARCHAR(40),
    OPTION07   VARCHAR(40),
    OPTION08   VARCHAR(40),
    OPTION09   VARCHAR(40),
    OPTION10   VARCHAR(40),
    PRIMARY KEY (CODE_ID, CODE_VALUE, LANG)
);

-- コードパターン
CREATE TABLE IF NOT EXISTS CODE_PATTERN (
    CODE_ID    VARCHAR(8) NOT NULL,
    CODE_VALUE VARCHAR(2) NOT NULL,
    LANG       VARCHAR(2) NOT NULL,
    PATTERN01  VARCHAR(1),
    PATTERN02  VARCHAR(1),
    PATTERN03  VARCHAR(1),
    PATTERN04  VARCHAR(1),
    PATTERN05  VARCHAR(1),
    PATTERN06  VARCHAR(1),
    PATTERN07  VARCHAR(1),
    PATTERN08  VARCHAR(1),
    PATTERN09  VARCHAR(1),
    PATTERN10  VARCHAR(1),
    PATTERN11  VARCHAR(1),
    PATTERN12  VARCHAR(1),
    PATTERN13  VARCHAR(1),
    PATTERN14  VARCHAR(1),
    PATTERN15  VARCHAR(1),
    PATTERN16  VARCHAR(1),
    PATTERN17  VARCHAR(1),
    PATTERN18  VARCHAR(1),
    PATTERN19  VARCHAR(1),
    PATTERN20  VARCHAR(1),
    PRIMARY KEY (CODE_ID, CODE_VALUE, LANG),
    FOREIGN KEY (CODE_ID, CODE_VALUE, LANG) REFERENCES CODE_NAME(CODE_ID, CODE_VALUE, LANG)
);

-- プロジェクトワーク (batch import staging)
CREATE TABLE IF NOT EXISTS PROJECT_WORK (
    PROJECT_WORK_ID    SERIAL       PRIMARY KEY,
    PROJECT_ID         INTEGER,
    PROJECT_NAME       VARCHAR(128) NOT NULL,
    PROJECT_TYPE       VARCHAR(2)   NOT NULL,
    PROJECT_CLASS      VARCHAR(2)   NOT NULL,
    PROJECT_START_DATE DATE         NOT NULL,
    PROJECT_END_DATE   DATE         NOT NULL,
    ORGANIZATION_ID    INTEGER      NOT NULL,
    CLIENT_ID          INTEGER      NOT NULL,
    PROJECT_MANAGER    VARCHAR(128) NOT NULL,
    PROJECT_LEADER     VARCHAR(128) NOT NULL,
    NOTE               VARCHAR(512),
    SALES              INTEGER
);

-- メール送信要求
CREATE TABLE IF NOT EXISTS MAIL_REQUEST (
    MAIL_REQUEST_ID   VARCHAR(20)   PRIMARY KEY,
    SUBJECT           VARCHAR(100),
    MAIL_FROM         VARCHAR(256),
    REPLY_TO          VARCHAR(256),
    RETURN_PATH       VARCHAR(256),
    CHARSET           VARCHAR(20),
    STATUS            VARCHAR(1),
    REQUEST_DATETIME  TIMESTAMP,
    SEND_DATETIME     TIMESTAMP,
    MAIL_BODY         VARCHAR(1000)
);

-- メール送信先
CREATE TABLE IF NOT EXISTS MAIL_RECIPIENT (
    MAIL_REQUEST_ID VARCHAR(20) NOT NULL REFERENCES MAIL_REQUEST(MAIL_REQUEST_ID),
    SERIAL_NUMBER   INTEGER     NOT NULL,
    RECIPIENT_TYPE  VARCHAR(1),
    MAIL_ADDRESS    VARCHAR(256),
    PRIMARY KEY (MAIL_REQUEST_ID, SERIAL_NUMBER)
);

-- メール添付ファイル
CREATE TABLE IF NOT EXISTS MAIL_ATTACHED_FILE (
    MAIL_REQUEST_ID VARCHAR(20)  NOT NULL REFERENCES MAIL_REQUEST(MAIL_REQUEST_ID),
    SERIAL_NUMBER   INTEGER      NOT NULL,
    FILE_NAME       VARCHAR(100),
    CONTENT_TYPE    VARCHAR(30),
    ATTACHED_FILE   BYTEA,
    PRIMARY KEY (MAIL_REQUEST_ID, SERIAL_NUMBER)
);

-- メールテンプレート
CREATE TABLE IF NOT EXISTS MAIL_TEMPLATE (
    MAIL_TEMPLATE_ID VARCHAR(10)  NOT NULL,
    LANG             VARCHAR(2)   NOT NULL,
    SUBJECT          VARCHAR(100),
    CHARSET          VARCHAR(20),
    MAIL_BODY        VARCHAR(1000),
    PRIMARY KEY (MAIL_TEMPLATE_ID, LANG)
);

-- 常駐バッチ状態管理
CREATE TABLE IF NOT EXISTS RESIDENT_BATCH_STATE (
    JOB_ID  VARCHAR(20) PRIMARY KEY,
    RUNNING BOOLEAN     NOT NULL DEFAULT FALSE
);

-- ユーザ別従事プロジェクト作成要求
CREATE TABLE IF NOT EXISTS PROJECTS_BY_USER_REQUEST (
    ID                BIGSERIAL    PRIMARY KEY,
    STATUS            VARCHAR(2)   NOT NULL,
    REQUEST_DATETIME  TIMESTAMP    NOT NULL,
    USER_ID           INTEGER      NOT NULL REFERENCES USERS(USER_ID)
);

-- ユーザ別従事プロジェクト
CREATE TABLE IF NOT EXISTS PROJECTS_BY_USER (
    USER_ID           INTEGER      PRIMARY KEY REFERENCES USERS(USER_ID),
    REQUEST_ID        BIGINT       REFERENCES PROJECTS_BY_USER_REQUEST(ID),
    FILE_NAME         VARCHAR(50),
    CREATE_DATETIME   TIMESTAMP,
    EXPIRE_DATETIME   TIMESTAMP
);

